from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from backend.routes.plan import router as plan_router

app = FastAPI(title="Structura.ai Backend", version="1.0.0")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(plan_router)


@app.get("/api/health")
async def health():
    return {"status": "ok", "service": "structura-backend"}
